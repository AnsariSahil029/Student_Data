    public void updateDB()
    {
        int q,i;
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            String sql =  "select * from details";
            con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/register_info","root","");
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            ResultSetMetaData stData= rs.getMetaData();
            q=stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)TableJ.getModel();
            RecordTable.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector columnData= new Vector();
                
                for(i=1;i<=q;i++)
                {
                    columnData.add(rs.getString("ID"));
                    columnData.add(rs.getString("Enroll"));
                    columnData.add(rs.getString("Class"));
                    columnData.add(rs.getString("School"));
                    columnData.add(rs.getString("Year"));
                    columnData.add(rs.getString("Marks"));
                }
                    
                    RecordTable.addRow(columnData);
                  
            }
            
            
            
            
        } 
          catch (Exception ex)
          {
               JOptionPane.showMessageDialog(null, ex);
          }
        
    }    
