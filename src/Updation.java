    private void UpdateJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateJActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)TableJ.getModel();
        int SelectedRows= TableJ.getSelectedRow();
        try
        {
            int id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());

            Class.forName("com.mysql.jdbc.Driver");
            con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/register_info","root","");

            pst = con.prepareStatement("UPDATE `details` SET `Enroll`=?,`Class`=?,`School`=?,`Year`=?,`Marks`=? where id=?");
            pst.setString(1,EnrollJ.getText());
            pst.setString(2,ClassJ.getText());
            pst.setString(3,SchoolJ.getText());
            pst.setString(4,YearJ.getText());
            pst.setString(5,MarksJ.getText());
            pst.setInt(6, id);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Record Updated Successfully");
            updateDB();

        }
        catch(SQLException E)
        {
            System.err.println(E);
        }
        catch (ClassNotFoundException E)
        {
            java.util.logging.Logger.getLogger(Updation.class.getName()).log(java.util.logging.Level.SEVERE, null, E);
        }

    }//GEN-LAST:event_UpdateJActionPerformed

