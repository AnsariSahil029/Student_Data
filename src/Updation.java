    private void DeleteJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteJActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)TableJ.getModel();
        int SelectedRows= TableJ.getSelectedRow();
        try
        {
            int id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            int deleteItem = JOptionPane.showConfirmDialog(null, "Confirm if you want to Delete Item",
                "Warning",JOptionPane.YES_NO_OPTION);
            if(deleteItem==JOptionPane.YES_OPTION)
            {

                Class.forName("com.mysql.jdbc.Driver");
                con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/register_info","root","");

                pst = con.prepareStatement("delete from details where ID = ?");

                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"Record Deleted Successfully");
                updateDB();
                EnrollJ.setText("");
                EnrollJ.requestFocus();
                ClassJ.setText("");
                SchoolJ.setText("");
                YearJ.setText("");
                MarksJ.setText("");
            }

        }
        catch(SQLException E)
        {
            System.err.println(E);
        }
        catch (ClassNotFoundException E)
        {
            java.util.logging.Logger.getLogger(Updation.class.getName()).log(java.util.logging.Level.SEVERE, null, E);
        }

    }//GEN-LAST:event_DeleteJActionPerformed

